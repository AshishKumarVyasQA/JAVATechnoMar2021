/*Program 4:  write a program which will print characters between ascii value 97 to 122.*/

package ravindra_J.Assignment_No_18_AsciiValueOperations;

public class CharOfAsciiValue97to122 {

	public static void main(String[] args) {
		
		for(int i = 97;i<=122;i++) {
			System.out.println(i+ " is the ASCII value of character "+(char)i);
		}
		
	}

}
