package meenu.assignment_18;

/*Program 4:  write a program which will print characters between ASCII value 97 to 122.
 */

public class CharBetweenAsciiValue {

	public static void main(String[] args) {
		System.out.println("Characters between ASCII value 97 to 122 are: ");
		for(int value = 97;value <= 122;value++) {
			//System.out.println("Characters between ASCII value 97 to 122 are: ");
			System.out.println((char)value);
		}

	}

}
